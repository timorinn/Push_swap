#include "libft.h"

static void	ft_print_rrb(int *a, int *b, int p)
{
	if (p % 10 == 1 && p / 10 && ft_is_sort(a) && !b[0])
		ft_putstr("\033[1;31mrrb\n\033[0m");
	else if (p % 10 == 1)
		ft_putstr("rrb\n");
	else if (p % 10 == 2 && p / 10 && ft_is_sort(a) && !b[0])
	{
		ft_putstr("\033[1;31m ______   ______   ______ \n\
|  __  \\ |  __  \\ |  __  \\\n| |__| | | |__| | | |__| /\n\
|    _/  |    _/  |  __  \\\n| |\\ \\   | |\\ \\   | |__| |\n\
|_| \\_\\  |_| \\_\\  |______/\n\n\033[0m");
		ft_print_ab(a, b);
	}
	else if (p % 10 == 2)
	{
		ft_putstr(" ______   ______   ______ \n\
|  __  \\ |  __  \\ |  __  \\\n| |__| | | |__| | | |__| /\n\
|    _/  |    _/  |  __  \\\n| |\\ \\   | |\\ \\   | |__| |\n\
|_| \\_\\  |_| \\_\\  |______/\n\n");
		ft_print_ab(a, b);
	}
}

int			ft_rrb(int *a, int *b, int p)
{
	int		i;
	int		copy;

	if ((i = b[0]) > 1)
	{
		i = b[0];
		copy = b[b[0]];
		while (i > 1)
		{
			b[i] = b[i - 1];
			i--;
		}
		b[1] = copy;
	}
	ft_print_rrb(a, b, p);
	return (1);
}
