#include "libft.h"

int		ft_rrb(int *b, int p)
{
	int		i;
	int		copy;

	i = b[0];
	copy = b[b[0]];
	while (i > 1)
	{
		b[i] = b[i - 1];
		i--;
	}
	b[1] = copy;
	if (p)
		ft_putstr("rrb\n");
	return (1);
}