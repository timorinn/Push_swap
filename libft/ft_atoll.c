#include "libft.h"

long long int	ft_atoll(const char *s)
{
	long long int	n;
	int				sign;

	sign = 1;
	n = 0;
	while ((*s >= 9 && *s <= 13) || *s == ' ')
		s++;
	if (*s == '+' || *s == '-')
		sign *= (*s++ == '-' ? -1 : 1);
	while (*s >= '0' && *s <= '9')
		n = n * 10 + (*s++) - 48;
	return (sign * n);
}
