#include <stdio.h>

void	ft_print_ab(int *a, int *b)
{
	int		i;
	int		max;

	i = 0;
	max = (a[0] >= b[0] ? a[0] : b[0]);
	printf("\n_________________________\n");
	while (i <= max)
	{
		printf("a[%d] = %3d   |   b[%d] = %3d\n", i, a[i], i, b[i]);
		if (i == 0)
			printf("\n");
		i++;
	}
	printf("_________________________\n");
}