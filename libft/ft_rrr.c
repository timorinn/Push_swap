#include "libft.h"

int		ft_rrr(int *a, int *b, int p)
{
	ft_rra(a, b, 0);
	ft_rrb(a, b, 0);
	if (p % 10 == 1 && p / 10 && ft_is_sort(a) && !b[0])
		ft_putstr("\033[1;31mrrr\n\033[1;37m");
	else if (p % 10 == 1)
		ft_putstr("rrr\n");
	else if (p % 10 == 2 && p / 10 && ft_is_sort(a) && !b[0])
	{
		ft_putstr("\033[1;31m ______   ______   ______ \n\
|  __  \\ |  __  \\ |  __  \\\n| |__| | | |__| | | |__| |\n\
|    _/  |    _/  |    _/\n| |\\ \\   | |\\ \\   | |\\ \\\n\
|_| \\_\\  |_| \\_\\  |_| \\_\\\n\n\033[1;37m");
		ft_print_ab(a, b);
	}
	else if (p % 10 == 2)
	{
		ft_putstr(" ______   ______   ______ \n\
|  __  \\ |  __  \\ |  __  \\\n| |__| | | |__| | | |__| |\n\
|    _/  |    _/  |    _/\n| |\\ \\   | |\\ \\   | |\\ \\\n\
|_| \\_\\  |_| \\_\\  |_| \\_\\\n\n");
		ft_print_ab(a, b);
	}
	return (1);
}
