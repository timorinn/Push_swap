#include "libft.h"

int		ft_rra(int *a, int p)
{
	int		i;
	int		copy;

	i = a[0];
	copy = a[a[0]];
	while (i > 1)
	{
		a[i] = a[i - 1];
		i--;
	}
	a[1] = copy;
	if (p)
		ft_putstr("rra\n");
	return (1);
}
