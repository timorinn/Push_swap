#include "libft.h"

int		ft_ra(int *a)
{
	int		i;
	int		copy;

	i = 1;
	copy = a[1];
	while (i < a[0])
	{
		a[i] = a[i + 1];
		i++;
	}
	a[a[0]] = copy;
	ft_putstr("ra\n");
	return (1);
}
