#include "libft.h"

int		ft_ss(int *a, int *b)
{
	a[1] = a[1] - a[2];
	a[2] = a[2] + a[1];
	a[1] = a[2] - a[1];
	b[1] = b[1] - b[2];
	b[2] = b[2] + b[1];
	b[1] = b[2] - b[1];
	ft_putstr("ss\n");
	return (1);
}