int		ft_ss(int *a, int *b)
{
	if (a[0] > 1 && b[0] > 1)
	{
		a[1] = a[1] - a[2];
		a[2] = a[2] + a[1];
		a[1] = a[2] - a[1];
		b[1] = b[1] - b[2];
		b[2] = b[2] + b[1];
		b[1] = b[2] - b[1];
		return (1);
	}
	return (0);
}