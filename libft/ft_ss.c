#include "libft.h"

int		ft_ss(int *a, int *b, int p)
{
	ft_sa(a, b, 0);
	ft_sb(a, b, 0);
	if (p % 10 == 1 && p / 10 && ft_is_sort(a) && !b[0])
		ft_putstr("\033[1;31mss\n\033[0m");
	else if (p % 10 == 1)
		ft_putstr("ss\n");
	else if (p % 10 == 2 && p / 10 && ft_is_sort(a) && !b[0])
	{
		ft_putstr("\033[1;31m ______   ______ \n\
/  __  \\ /  __  \\\n| |  |_| | |  |_|\n\
| |___   | |___\n\
\\____  \\ \\____  \\\n _   | |  _   | |\n\
| |__| | | |__| |\n\\______/ \\______/\n\n\033[0m");
		ft_print_ab(a, b);
	}
	else if (p % 10 == 2)
	{
		ft_putstr(" ______   ______ \n\
/  __  \\ /  __  \\\n| |  |_| | |  |_|\n\
| |___   | |___\n\
\\____  \\ \\____  \\\n _   | |  _   | |\n\
| |__| | | |__| |\n\\______/ \\______/\n\n");
		ft_print_ab(a, b);
	}
	return (1);
}
