#include "libft.h"

int		ft_ss(int *a, int *b, int p)
{
	a[1] = a[1] - a[2];
	a[2] = a[2] + a[1];
	a[1] = a[2] - a[1];
	b[1] = b[1] - b[2];
	b[2] = b[2] + b[1];
	b[1] = b[2] - b[1];
	if (p == 1)
		ft_putstr("ss\n");
	else if (p == 2)
	{
		ft_putstr("\n ______   ______ \n\
/  __  \\ /  __  \\\n| |  |_| | |  |_|\n\
| |___   | |___\n\
\\____  \\ \\____  \\\n _   | |  _   | |\n\
| |__| | | |__| |\n\\______/ \\______/\n\n");
		ft_print_ab(a, b);
	}
	return (1);
}
