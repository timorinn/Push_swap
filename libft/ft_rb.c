int		ft_rb(int *b)
{
	int		i;
	int		copy;

	i = 1;
	copy = b[1];
	while (i < b[0])
	{
		b[i] = b[i + 1];
		i++;
	}
	b[b[0]] = copy;
	return (1);
}
