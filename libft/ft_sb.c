#include "libft.h"

int		ft_sb(int *a, int *b, int p)
{
	b[1] = b[1] - b[2];
	b[2] = b[2] + b[1];
	b[1] = b[2] - b[1];
	if (p == 1)
		ft_putstr("sb\n");
	else if (p == 2)
	{
		ft_putstr("\n ______   ______ \n\
/  __  \\ |  __  \\\n| |  |_| | |__| /\n\
| |___   |  __  \\\n\\____  \\ | |__| |\n _   | | |______/\n| |__| |\n\
\\______/\n\n");
		ft_print_ab(a, b);
	}
	return (1);
}
