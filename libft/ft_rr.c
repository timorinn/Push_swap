#include "libft.h"

static void	ft_print_rr(int *a, int *b, int p)
{
	if (p % 10 == 1 && p / 10 && ft_is_sort(a) && !b[0])
		ft_putstr("\033[1;31mrr\n\033[1;37m");
	else if (p % 10 == 1)
		ft_putstr("rr\n");
	else if (p % 10 == 2 && p / 10 && ft_is_sort(a) && !b[0])
	{
		ft_putstr("\033[1;31m ______   ______ \n|  __  \\ |  __  \\\n\
| |__| | | |__| |\n|    _/  |    _/\n| |\\ \\   | |\\ \\\n\
|_| \\_\\  |_| \\_\\\n\n\033[1;37m");
		ft_print_ab(a, b);
	}
	else if (p % 10 == 2)
	{
		ft_putstr(" ______   ______ \n|  __  \\ |  __  \\\n\
| |__| | | |__| |\n|    _/  |    _/\n| |\\ \\   | |\\ \\\n\
|_| \\_\\  |_| \\_\\\n\n");
		ft_print_ab(a, b);
	}
}

int			ft_rr(int *a, int *b, int p)
{
	ft_ra(a, b, 0);
	ft_rb(a, b, 0);
	ft_print_rr(a, b, p);
	return (1);
}
