#include "libft.h"

int		ft_check_borders(int *a)
{
	int i;
	int j;

	i = 1;
	while (i != a[0])
	{
		j = i + 1;
		while (j <= a[0])
		{
			if (a[i] == a[j])
				return (0);
			j++;
		}
		i++;
	}
	return (1);
}
