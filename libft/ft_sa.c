#include "libft.h"

int		ft_sa(int *a, int p)
{
	a[1] = a[1] - a[2];
	a[2] = a[2] + a[1];
	a[1] = a[2] - a[1];
	if (p)
		ft_putstr("sa\n");
	return (1);
}
